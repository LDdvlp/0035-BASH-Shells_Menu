#!/usr/bin/env bash

print_ascii_banner() {
    local key="$1"

    case "$key" in
        gitbash-bash)
            cat <<'EOF'
   ____ _ _     ____            _           __    ____            _     
  / ___(_) |_  | __ )  __ _ ___| |__        \ \  | __ )  __ _ ___| |__  
 | |  _| | __| |  _ \ / _` / __| '_ \   _____\ \ |  _ \ / _` / __| '_ \ 
 | |_| | | |_  | |_) | (_| \__ \ | | | |_____/ / | |_) | (_| \__ \ | | |
  \____|_|\__| |____/ \__,_|___/_| |_|      /_/  |____/ \__,_|___/_| |_|
                                                          
           Git Bash  路  Bash
EOF
            ;;
        gitbash-zsh)
            cat <<'EOF'
   ____ _ _     ____            _           __    _____    _     
  / ___(_) |_  | __ )  __ _ ___| |__        \ \  |__  /___| |__  
 | |  _| | __| |  _ \ / _` / __| '_ \   _____\ \   / // __| '_ \ 
 | |_| | | |_  | |_) | (_| \__ \ | | | |_____/ /  / /_\__ \ | | |
  \____|_|\__| |____/ \__,_|___/_| |_|      /_/  /____|___/_| |_|
                                                                 
           Git Bash  路  Zsh
EOF
            ;;
        wsl-bash)
            cat <<'EOF'
 __        ______  _           __    ____            _     
 \ \      / / ___|| |          \ \  | __ )  __ _ ___| |__  
  \ \ /\ / /\___ \| |      _____\ \ |  _ \ / _` / __| '_ \ 
   \ V  V /  ___) | |___  |_____/ / | |_) | (_| \__ \ | | |
    \_/\_/  |____/|_____|      /_/  |____/ \__,_|___/_| |_|

       WSL (Linux) 路 Bash
EOF
            ;;
        wsl-zsh)
            cat <<'EOF'
 __        ______  _           __    _____    _     
 \ \      / / ___|| |          \ \  |__  /___| |__  
  \ \ /\ / /\___ \| |      _____\ \   / // __| '_ \ 
   \ V  V /  ___) | |___  |_____/ /  / /_\__ \ | | |
    \_/\_/  |____/|_____|      /_/  /____|___/_| |_|
                                                    
        WSL (Linux) 路 Zsh
EOF
            ;;
        *)
            echo "=== Shell selection ==="
            ;;
    esac
}
